class Solution {
  public:
    vector<int> removeDuplicate(vector<int>& a) {
        unordered_map<int,int>m;
        vector<int>ans;
        for(int i:a){
            if(m.find(i)==m.end()){
                ans.push_back(i);
                m[i]=1;
            }
        }
        return ans;
    }
};

