class Solution {

  public:
    vector<int> alternateSort(vector<int>& a) {
        sort(a.begin(),a.end());
        int i=0,n=a.size();
        vector<int>ans(n,0);
        for(int j=1;j<n;j+=2){
            ans[j]=a[i++];
        }
        i=n-1;
        for(int j=0;j<n;j+=2){
            ans[j]=a[i--];
        }
        return ans;
    }
};


